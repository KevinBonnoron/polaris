// NotifyInput is the producer-side payload accepted by Notify. The title is
// rendered from TitleTemplate + Vars at fire time so callers can re-use a
// templated string across many fires without manual sprintf.
type NotifyInput struct {
	ProjectID     string
	Type          NotificationType
	Severity      NotificationSeverity
	TitleTemplate string
	Vars          map[string]string
	Payload       map[string]any
}

// Notify is the single funnel for raising a notification. It applies the
// user's per-kind preferences before persisting, then dispatches an OS toast
// when both the OS channel is enabled and the user hasn't muted everything.
func (s *Service) Notify(in NotifyInput) (Notification, error) {
	if s.store == nil {
		return Notification{}, errors.New("store not initialised")
	}
	if in.Severity == "" {
		in.Severity = SeverityInfo
	}
	n := Notification{
		ProjectID: in.ProjectID,
		Type:      in.Type,
		Severity:  in.Severity,
		Title:     renderTemplate(in.TitleTemplate, in.Vars),
		Payload:   in.Payload,
		CreatedAt: time.Now().Unix(),
	}
	settings, _ := s.store.GetNotificationSettings()
	if !eventEnabled(settings.Events, n) {
		return Notification{}, nil
	}
	saved, err := s.store.UpsertNotification(n)
	if err != nil {
		return saved, err
	}
	if !settings.SilenceAll && settings.OSEnabled && !s.isAppFocused() {
		go dispatchOSNotification(saved, settings)
	}
	return saved, nil
}

