type Agent struct {
	ID        string `json:"id"`
	ProjectID string `json:"projectId"`
	Kind      string `json:"kind"`
	// Source is "manual" (default) when the user spawned the agent from the
	// UI, or "automation" when an Automation rule triggered the run.
	Source string `json:"source"`
	// Summary is a short description of the agent's task, generated by the CLI
	// after the first turn (e.g. claude's `summary` of the user's request).
	// Empty until the first turn produces text.
	Summary   string `json:"summary"`
	Status    string `json:"status"`
	StartedAt int64  `json:"startedAt"`
	UpdatedAt int64  `json:"updatedAt"`
	// PID is the OS process id of the running agent subprocess, persisted so
	// that startup recovery (RecoverInterruptedAgents) can tell a genuinely
	// dead agent from one still running in another live app instance sharing
	// the same database. 0 when no subprocess is attached.
	PID       int    `json:"-"`
	SessionID string `json:"sessionId"`
	// Model is the CLI model chosen at spawn time. Needed for deferred-start
	// agents (created with empty task) so the first Send can pass it to the
	// initial spawn command.
	Model string `json:"model,omitempty"`
	// ProviderID is set when the agent runs against a user-defined custom
	// provider (Kind == "opencode"). It points back to the CustomProvider so
	// the UI can resolve the provider's name/colour for display.
	ProviderID string `json:"providerId,omitempty"`
	// Tokens is the per-category usage accumulated across the agent's turns. The
	// total is derived (Tokens.Total()) rather than stored, so the UI can show
	// the breakdown and explain why a total is large (cache re-reads of the
	// fixed context usually dominate the actual new work).
	Tokens        TokenUsage `json:"tokens"`
	CostUSD       float64    `json:"costUsd"`
	FilesModified int        `json:"filesModified"`
	ToolsUsed     int        `json:"toolsUsed"`
	// Worktree carries the git-isolation details when the agent runs on a
	// dedicated branch inside its own worktree (ticket-triggered or manually
	// isolated spawns). Zero value when the agent worked in the project root.
	Worktree Worktree `json:"worktree"`
	// PendingQuestion holds an unanswered AskUserQuestion tool call. Persisted
	// so the choices survive a closed modal or an app restart mid-question.
	// nil when no question is pending.
	PendingQuestion *PendingQuestion `json:"pendingQuestion,omitempty"`
	// AllowedTools restricts which claude-code tools are injected into the
	// system prompt. Empty means all tools (no restriction). Only honoured
	// for the claude-code kind; other backends manage their own tool lists.
	AllowedTools []string `json:"allowedTools,omitempty"`
}

// TokenUsage breaks an agent's token consumption into its categories. The total
// is derived via Total() rather than stored separately, since the categories
// always sum to it. cacheRead (the fixed context replayed each turn) usually
// dominates the few "new" input/output tokens.
type TokenUsage struct {
	Input         int `json:"input"`
	Output        int `json:"output"`
	CacheCreation int `json:"cacheCreation"`
	CacheRead     int `json:"cacheRead"`
}

// Total is the sum of all token categories.
func (t TokenUsage) Total() int {
	return t.Input + t.Output + t.CacheCreation + t.CacheRead
}

// Add returns the element-wise sum of two usages.
func (t TokenUsage) Add(o TokenUsage) TokenUsage {
	return TokenUsage{
		Input:         t.Input + o.Input,
		Output:        t.Output + o.Output,
		CacheCreation: t.CacheCreation + o.CacheCreation,
		CacheRead:     t.CacheRead + o.CacheRead,
	}
}

