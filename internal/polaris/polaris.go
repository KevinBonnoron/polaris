// SetAppFocused records whether the app window is currently focused. Used to
// gate OS toast dispatching: the bell still updates while focused, but the
// native toast is suppressed since the user is already in the app.
func (s *Service) SetAppFocused(focused bool) {
	s.focusMu.Lock()
	s.appFocused = focused
	s.focusMu.Unlock()
}

func (s *Service) isAppFocused() bool {
	s.focusMu.RLock()
	defer s.focusMu.RUnlock()
	return s.appFocused
}

// eventEnabled answers "should this notification be surfaced?" against the
// user's per-category preferences.
func eventEnabled(flags NotificationEventFlags, n Notification) bool {
	switch n.Type {
	case NotifTypeAgent:
		event, _ := n.Payload["event"].(string)
		switch AgentEvent(event) {
		case AgentEventWaiting:
			return flags.Waiting
		case AgentEventCompleted:
			// Both successful and failed terminal states route through here;
			// failures are split out via severity.
			if n.Severity == SeverityError {
				return flags.Errored
			}
			return flags.Completed
		default:
			return true
		}
	case NotifTypeAutomation:
		return flags.Automation
	case NotifTypeUser:
		return flags.User
	default:
		return true
	}
}

