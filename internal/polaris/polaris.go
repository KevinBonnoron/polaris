// AutomationTrigger is the discriminated payload describing what conditions
// fire an automation. The wire format is JSON so the frontend can evolve the
// shape without forcing a Go-side migration on every change.
type AutomationTrigger struct {
	// Kind is one of: "tickets.transition", "repository.pr_opened",
	// "repository.pr_comment", "repository.pr_approved",
	// "repository.pr_build_failed", "repository.pr_build_success",
	// "repository.issue_assigned", "sentry.new_issue",
	// "dokploy.deployment_failed", "dokploy.deployment_succeeded".
	Kind string `json:"kind"`
	// FromStatusIDs is the set of source statuses accepted ([] = any). For
	// "tickets.transition".
	FromStatusIDs []string `json:"fromStatusIds,omitempty"`
	// ToStatusID is the destination status (required for tickets.transition).
	ToStatusID string `json:"toStatusId,omitempty"`
	// IncludeDrafts, when true, lets repository.pr_opened fire for draft PRs.
	IncludeDrafts bool `json:"includeDrafts,omitempty"`
	// AuthorFilter restricts repository.pr_opened to a specific GitHub login.
	// Empty means any author.
	AuthorFilter string `json:"authorFilter,omitempty"`
	// MinLevel is the minimum Sentry severity that fires sentry.new_issue
	// ("warning" | "error" | "fatal"). Empty means any level.
	MinLevel string `json:"minLevel,omitempty"`
}

// AutomationAction is the discriminated payload describing one action to run
// when the trigger fires. Multiple actions execute in declaration order; a
// failing action only logs and does not halt the chain.
type AutomationAction struct {
	// Kind is one of: "spawn_agent", "resume_pr_agent", "tickets_transition", "notification", "send_email", "send_message", "trigger_workflow".
	Kind string `json:"kind"`

	// spawn_agent fields
	AgentKind    string `json:"agentKind,omitempty"`
	Model        string `json:"model,omitempty"`
	TaskTemplate string `json:"taskTemplate,omitempty"`

	// tickets_transition fields. TicketIssueKey is a template (e.g. "{{key}}");
	// empty means "use the issue key from the trigger context", which for
	// repository triggers is auto-extracted from the branch or PR title.
	TicketIssueKey   string `json:"ticketsIssueKey,omitempty"`
	TicketToStatusID string `json:"ticketsToStatusId,omitempty"`

	// notification fields
	NotifyTitle string `json:"notifyTitle,omitempty"` // template
	NotifyKind  string `json:"notifyKind,omitempty"`  // "info" (default) | "warning" | "error"

	// send_email fields (requires the project's Resend integration to be configured)
	EmailTo      string `json:"emailTo,omitempty"`      // template
	EmailSubject string `json:"emailSubject,omitempty"` // template
	EmailBody    string `json:"emailBody,omitempty"`    // template, sent as plain text

	// send_message fields (requires a slack/discord/telegram integration)
	MessageProvider string `json:"messageProvider,omitempty"` // "slack" | "discord" | "telegram"
	MessageTitle    string `json:"messageTitle,omitempty"`    // template
	MessageBody     string `json:"messageBody,omitempty"`     // template

	// trigger_workflow fields (requires a repository integration)
	WorkflowFile   string `json:"workflowFile,omitempty"`   // workflow filename, e.g. "deploy.yml"; supports templates
	WorkflowRef    string `json:"workflowRef,omitempty"`    // branch/tag ref; supports templates
	WorkflowInputs string `json:"workflowInputs,omitempty"` // "KEY=value\nKEY2=value2"; supports templates
}

