// NotificationSeverity tags every notification with how concerning it is.
// The frontend uses it for tone/colour and (eventually) per-severity
// settings. Adding a value requires a matching entry in src/db/types.ts.
type NotificationSeverity string

const (
	SeverityInfo    NotificationSeverity = "info"
	SeveritySuccess NotificationSeverity = "success"
	SeverityWarning NotificationSeverity = "warning"
	SeverityError   NotificationSeverity = "error"
)

// NotificationType discriminates the source of the notification. Each value
// determines the shape of Payload (see Agent/Automation/User payload types).
type NotificationType string

const (
	NotifTypeAgent      NotificationType = "agent"
	NotifTypeAutomation NotificationType = "automation"
	NotifTypeUser       NotificationType = "user"
)

// AgentEvent narrows what happened on an agent-typed notification. Stored
// inside the agent payload, not at the top level, because automation/user
// notifications don't have an equivalent.
type AgentEvent string

const (
	AgentEventWaiting   AgentEvent = "waiting"
	AgentEventCompleted AgentEvent = "completed"
)

// AgentNotificationPayload is the JSON shape persisted in payloadJson when
// Type == NotifTypeAgent.
type AgentNotificationPayload struct {
	AgentID string     `json:"agentId"`
	Event   AgentEvent `json:"event"`
}

// AutomationNotificationPayload is the JSON shape persisted in payloadJson
// when Type == NotifTypeAutomation.
type AutomationNotificationPayload struct {
	AutomationID string `json:"automationId,omitempty"`
}

type Notification struct {
	ID        string               `json:"id"`
	ProjectID string               `json:"projectId"`
	Type      NotificationType     `json:"type"`
	Severity  NotificationSeverity `json:"severity"`
	Title     string               `json:"title"`
	Payload   map[string]any       `json:"payload,omitempty"`
	CreatedAt int64                `json:"createdAt"`
	Read      bool                 `json:"read"`
}

// AgentID is a convenience accessor for payload["agentId"] when Type=agent.
// Returns "" when the notification isn't agent-typed or the field is absent.
func (n Notification) AgentID() string {
	if n.Type != NotifTypeAgent || n.Payload == nil {
		return ""
	}
	if v, ok := n.Payload["agentId"].(string); ok {
		return v
	}
	return ""
}

// NotificationEventFlags toggles in-app and OS surfacing per category. A
// flag set to false drops the notification entirely (not persisted, no OS
// toast). The agent flags (Waiting/Completed/Errored/Started) gate
// agent-typed notifications by AgentEvent + severity; Automation and User
// gate everything emitted under their respective NotificationType.
type NotificationEventFlags struct {
	Waiting    bool `json:"waiting"`
	Completed  bool `json:"completed"`
	Errored    bool `json:"errored"`
	Started    bool `json:"started"`
	Automation bool `json:"automation"`
	User       bool `json:"user"`
}

