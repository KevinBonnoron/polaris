// AutomationTrigger is the discriminated payload describing what conditions
// fire an automation. The wire format is JSON so the frontend can evolve the
// shape without forcing a Go-side migration on every change.
type AutomationTrigger struct {
	// Kind is one of: "jira.transition", "repository.pr_opened".
	Kind string `json:"kind"`
	// FromStatusIDs is the set of source statuses accepted ([] = any). For
	// "jira.transition".
	FromStatusIDs []string `json:"fromStatusIds,omitempty"`
	// ToStatusID is the destination status (required for jira.transition).
	ToStatusID string `json:"toStatusId,omitempty"`
	// Assignee filter: "me" / "any" / specific displayName.
	Assignee string `json:"assignee,omitempty"`
	// AlsoOnReassignment fires even when the status didn't change, as long as
	// the assignee just became the configured one.
	AlsoOnReassignment bool `json:"alsoOnReassignment,omitempty"`
	// IncludeDrafts, when true, lets repository.pr_opened fire for draft PRs.
	IncludeDrafts bool `json:"includeDrafts,omitempty"`
	// AuthorFilter restricts repository.pr_opened to a specific GitHub login.
	// Empty means any author.
	AuthorFilter string `json:"authorFilter,omitempty"`
	// ExcludeOwnComments, when true, skips comments authored by the current
	// gh-authenticated user on repository.pr_comment triggers.
	ExcludeOwnComments bool `json:"excludeOwnComments,omitempty"`
	// OnlyMine restricts repository.pr_build_failed / pr_build_success to PRs
	// authored by the current gh-authenticated user, and
	// repository.issue_assigned to issues newly assigned to that user.
	OnlyMine bool `json:"onlyMine,omitempty"`
}

// AutomationAction is the discriminated payload describing one action to run
// when the trigger fires. Multiple actions execute in declaration order; a
// failing action only logs and does not halt the chain.
type AutomationAction struct {
	// Kind is one of: "spawn_agent", "jira_transition", "notification".
	Kind string `json:"kind"`

	// spawn_agent fields
	AgentKind    string `json:"agentKind,omitempty"`
	Model        string `json:"model,omitempty"`
	TaskTemplate string `json:"taskTemplate,omitempty"`

	// jira_transition fields. JiraIssueKey is a template (e.g. "{{key}}");
	// empty means "use the issue key from the trigger context", which for
	// repository triggers is auto-extracted from the branch or PR title.
	JiraIssueKey   string `json:"jiraIssueKey,omitempty"`
	JiraToStatusID string `json:"jiraToStatusId,omitempty"`

	// notification fields
	NotifyTitle string `json:"notifyTitle,omitempty"` // template
	NotifyKind  string `json:"notifyKind,omitempty"`  // "info" (default) | "warning" | "error"
}

// Automation links a trigger to a list of actions. Snapshot holds the
// last-seen state used by the manager to detect deltas.
type Automation struct {
	ID              string             `json:"id"`
	ProjectID       string             `json:"projectId"`
	Name            string             `json:"name"`
	Enabled         bool               `json:"enabled"`
	Source          string             `json:"source"`
	Trigger         AutomationTrigger  `json:"trigger"`
	Actions         []AutomationAction `json:"actions"`
	PollIntervalSec int                `json:"pollIntervalSec"`
	SnapshotJSON    string             `json:"snapshotJson,omitempty"`
}

// AutomationRunOutcome describes whether the actions chain succeeded.
type AutomationRunOutcome string

const (
	RunOutcomeFired AutomationRunOutcome = "fired" // every action succeeded
	RunOutcomeError AutomationRunOutcome = "error" // at least one action errored
)

// ActionResult is one entry in AutomationRun.Actions, capturing how a single
// action of the automation chain played out.
type ActionResult struct {
	Kind   string `json:"kind"`
	Status string `json:"status"` // "ok" | "error" | "skipped"
	Detail string `json:"detail,omitempty"`
}

// AutomationRun is one row of the automations_runs audit log: a single fire
// event including which actions ran and what they returned. Persisted by
// the AutomationManager after each dispatched trigger match.
type AutomationRun struct {
	ID           string               `json:"id"`
	AutomationID string               `json:"automationId"`
	ProjectID    string               `json:"projectId"`
	StartedAt    int64                `json:"startedAt"`
	Outcome      AutomationRunOutcome `json:"outcome"`
	Reason       string               `json:"reason"`
	Actions      []ActionResult       `json:"actions"`
}

