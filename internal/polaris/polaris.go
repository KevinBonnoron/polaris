// IntegrationConfig holds the per-plugin settings for a single integration
// connected to a project. The shape is intentionally untyped: each plugin owns
// its own schema (defined client-side in the integration catalog) and is
// responsible for validating its own fields.
type IntegrationConfig map[string]any

type Project struct {
	ID           string                       `json:"id"`
	Name         string                       `json:"name"`
	Color        string                       `json:"color"`
	Path         string                       `json:"path"`
	Logo         string                       `json:"logo"`
	Integrations map[string]IntegrationConfig `json:"integrations"`
	// IsolatedDefault is the project-level default for new agents: when true,
	// the runner spawns the agent into a fresh git worktree on a generated
	// branch; when false, the agent works on the current branch in place.
	IsolatedDefault bool `json:"isolatedDefault"`
	// BranchPrefix is the prefix prepended to auto-generated branch names
	// when IsolatedDefault is on (e.g. "polaris/", "feat/"). Used only for
	// manual spawns — automation triggers carry their own branch name.
	BranchPrefix string `json:"branchPrefix"`
	// HasGit is computed at read time from the project path. Not persisted —
	// reflects the live filesystem so it stays correct after `git init` or
	// directory moves without requiring a manual refresh.
	HasGit bool `json:"hasGit"`
}

type Agent struct {
	ID        string `json:"id"`
	ProjectID string `json:"projectId"`
	Kind      string `json:"kind"`
	// Summary is a short description of the agent's task, generated by the CLI
	// after the first turn (e.g. claude's `summary` of the user's request).
	// Empty until the first turn produces text.
	Summary   string `json:"summary"`
	Status    string `json:"status"`
	StartedAt int64  `json:"startedAt"`
	Tokens    int    `json:"tokens"`
	// Token breakdown across the agent's turns. Tokens stays the sum of these
	// four; they're surfaced so the UI can explain why a total is large (cache
	// re-reads of the fixed context dominate the actual new work).
	TokensInput       int    `json:"tokensInput"`
	TokensOutput      int    `json:"tokensOutput"`
	TokensCacheCreate int    `json:"tokensCacheCreate"`
	TokensCacheRead   int    `json:"tokensCacheRead"`
	SessionID         string `json:"sessionId"`
	// Source is "manual" (default) when the user spawned the agent from the
	// UI, or "automation" when an Automation rule triggered the run. Used to
	// suppress lifecycle notifications for manually-launched agents — the
	// user is already watching their own spawn.
	Source        string  `json:"source"`
	CostUSD       float64 `json:"costUsd"`
	FilesModified int     `json:"filesModified"`
	ToolsUsed     int     `json:"toolsUsed"`
	// Branch and WorktreePath are populated when the agent runs in an isolated
	// git worktree (currently: Jira-triggered automations). When empty, the
	// agent worked directly inside the project's root.
	Branch       string `json:"branch,omitempty"`
	WorktreePath string `json:"worktreePath,omitempty"`
	// IssueKey carries the originating ticket (e.g. "ABC-123") so the PR title
	// and follow-up actions can reference it.
	IssueKey string `json:"issueKey,omitempty"`
	// PRURL is set once CreatePRForAgent successfully opened a pull request.
	PRURL string `json:"prUrl,omitempty"`
	// Model is the CLI model chosen at spawn time. Needed for deferred-start
	// agents (created with empty task) so the first Send can pass it to the
	// initial spawn command.
	Model string `json:"model,omitempty"`
	// PendingQuestionID is the tool_use id of an unanswered AskUserQuestion
	// tool call. Persisted so the choices remain visible if the modal was
	// closed when the question fired, or if the app restarts mid-question.
	PendingQuestionID string `json:"pendingQuestionId,omitempty"`
	// PendingQuestionInput is the JSON-encoded tool input (the questions
	// payload) shown to the user. Empty when no question is pending.
	PendingQuestionInput string `json:"pendingQuestionInput,omitempty"`
}

